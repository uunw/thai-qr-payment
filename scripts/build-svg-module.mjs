#!/usr/bin/env node
/**
 * Inline every SVG under packages/assets/src/svg/ into a generated
 * TypeScript module so consumers can import the markup as a string
 * without runtime fs access. Run as part of `pnpm build`.
 */

import { readdir, readFile, writeFile } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(here, '..');
const svgDir = join(repoRoot, 'packages/assets/src/svg');
const outFile = join(repoRoot, 'packages/assets/src/generated.ts');

const HEADER = `// Generated by scripts/build-svg-module.mjs — do not edit by hand.
// Run \`pnpm --filter @thai-qr-payment/assets build\` to regenerate.

`;

function toIdentifier(filename) {
  // PromptPay1.silhouette.svg → PromptPay1_silhouette
  return filename.replace(/\.svg$/, '').replace(/[^A-Za-z0-9]/g, '_');
}

async function main() {
  const files = (await readdir(svgDir)).filter((f) => f.endsWith('.svg')).sort();
  const constants = [];
  const colorRegistry = [];
  const silhouetteRegistry = [];

  for (const file of files) {
    const ident = toIdentifier(file);
    const raw = await readFile(join(svgDir, file), 'utf8');
    const escaped = JSON.stringify(raw);
    constants.push(`export const ${ident} = ${escaped};`);
    if (file.endsWith('.silhouette.svg')) {
      const stem = file.replace(/\.silhouette\.svg$/, '');
      silhouetteRegistry.push(`  ${JSON.stringify(stem)}: ${ident},`);
    } else {
      const stem = file.replace(/\.svg$/, '');
      colorRegistry.push(`  ${JSON.stringify(stem)}: ${ident},`);
    }
  }

  const body =
    `${HEADER}` +
    `${constants.join('\n')}\n\n` +
    `export const COLOR_LOGOS = {\n${colorRegistry.join('\n')}\n} as const;\n\n` +
    `export const SILHOUETTE_LOGOS = {\n${silhouetteRegistry.join('\n')}\n} as const;\n\n` +
    `export type ColorLogoName = keyof typeof COLOR_LOGOS;\n` +
    `export type SilhouetteLogoName = keyof typeof SILHOUETTE_LOGOS;\n`;

  await writeFile(outFile, body, 'utf8');
  console.log(`✓ Wrote ${outFile} (${files.length} SVGs inlined)`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
